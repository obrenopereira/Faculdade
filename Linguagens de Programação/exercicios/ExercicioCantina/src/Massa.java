public class Massa {

    public String tipoMassa;
    public double valor;

    public Massa(String tipoMassa, double valor) {
        this.tipoMassa = tipoMassa;
        this.valor = valor;
    }

    public String getTipoMassa() {
        return tipoMassa;
    }

    public double getValor() {
        return valor;
    }
}
